def is_palindrome(s):
    return s == s[::-1]

nums = [
    "7798338247658278460338648728567428338977",
    "425730490239344783972738974439320493524",
    "2704702208931031198668911301398022074072",
    "0974101607733149676776769413377061014790"
]

for num in nums:
    print(num, is_palindrome(num))
